void solve()
{
    int X, Y;
    cin >> X >> Y;

    int N = 0;

    if (X > Y)
    {
        N = 2 * X - 1;
    }
    else
    {
        N = 2 * Y - 1;
    }

    cout << N;
    newLine;
}

