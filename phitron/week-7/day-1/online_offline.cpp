void solve()
{
    int n, m;
    cin >> n >> m;

    float d_price = n * 0.1;

    float remaining = abs(n - d_price);

    if (m == remaining)
        cout << "EITHER\n";
    else if (m > remaining)
        cout << "ONLINE\n";
    else
        cout << "DINING\n";
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}