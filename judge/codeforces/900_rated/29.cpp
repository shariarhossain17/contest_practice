void solve()
{
    int s;
    cin >> s;

    ll ans = 0;

    while (true)
    {
        if (s < 10)
        {
            ans += s;
            break;
        }
        ans += s / 10 * 10;
        s = s / 10 + s % 10;
    }

    cout << ans;
    newLine;
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
