void solve()
{
    int n, s, t;
    cin >> n >> s >> t;

    if (s == n && t == n)
    {
        cout << 1 << endl;
        return;
    }

    int res = (n - min(s, t)) + 1;
    cout << res << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
