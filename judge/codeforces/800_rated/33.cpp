void solve()
{
    int n;
    cin >> n;

    long double diff_double = (long double)n;
    ll result = (ll)(diff_double / 2);

    if (result % 2 == 0 && result != 0)
        cout
            << result - 1 << "\n";
    else
        cout << result << "\n";
}
