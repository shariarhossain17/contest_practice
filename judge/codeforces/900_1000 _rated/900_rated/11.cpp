void solve()
{
    int n;
    cin >> n;

    for (int k = 2; k <= 30; k++)
    {
        int x = (1 << k) - 1;

        if ((n % x) == 0)
        {
            cout << n / x;
            newLine;
            return;
        }
    }
}
