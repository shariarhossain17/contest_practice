void solve()
{
    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i == j or i == j - 1)
                cout << 1 << " ";
            else if (i == (n - 1) and j == 0)
                cout << 1 << " ";
            else
                cout << 0 << " ";
        }

        cout << "\n";
    }
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
